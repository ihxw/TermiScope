  static String getText(String locale, String key) {
    final cleanLocale = locale.startsWith('zh') ? 'zh' : 'en';
    final parts = key.split('.');
    if (parts.length != 2) return key;
    final section = parts[0];
    final subKey = parts[1];

    final localeMap = _data[cleanLocale] ?? _data['zh']!;
    final sectionMap = localeMap[section];
    if (sectionMap == null) return key;
    return sectionMap[subKey] ?? key;
  }
